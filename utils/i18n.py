def t(key, **kwargs):
    """
    ترجمة سريعة من القاموس
    
    Usage:
        t('Save')  # → حفظ (if Arabic) or Save (if English)
        t('Hello {name}', name='Ahmad')
    """
    lang = get_current_language()
    
    if key in TRANSLATIONS:
        text = TRANSLATIONS[key].get(lang, key)
    else:
        text = key
    
    if kwargs:
        text = text.format(**kwargs)
    
    return text


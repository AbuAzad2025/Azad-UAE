"""
Regenerates migrations/versions/squash_001_baseline.py.

This is the source-of-truth generator for the squashed baseline. It needs an
autogenerated explicit schema first, so run it in TWO steps:

  Step 1 (autogenerate explicit schema into a throwaway migration):
    # move the active baseline + legacy/ out of migrations/versions/ so the
    # script dir is empty, then:
    ./.venv/Scripts/python.exe -m flask db migrate -m t --rev-id test1
    # (run with DATABASE_URL pointing at a FRESH empty database)
    # copy migrations/versions/test1_t.py aside, then restore baseline + legacy/

  Step 2 (build the real baseline from test1_t.py):
    ./.venv/Scripts/python.exe build_baseline.py
    # then DELETE migrations/versions/test1_t.py (it is only a temp input)

The generator emits:
  * Explicit, fully-compiled schema (op.create_table per table). Tables are
    created WITHOUT inline FKs; all foreign keys are added in a second pass
    after every table exists (handles cyclic dependencies).
  * Schema only -- NO tenant data is seeded. Tenants (e.g. demo) are provisioned
    by the dedicated seed commands at runtime.
"""

